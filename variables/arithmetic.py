a = 35
b = 98

# Arithmetic operator
print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)

# Assignment operator
c = 5
d = 6

print(c,d)
c += 5
d += 1
print(c, d)

# Comparison Operator
e = 5
f = 8

print(e==f)
print(e>f)
print(e<f)
print(e!=f)

# Logical Operator
print(e==f and e>f)
print(e==f and e<f)
print(not(e>f))